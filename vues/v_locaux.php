    <p>
        Notes : les salles Majorelle, Grüber, Lamour, Longwy, Daum, Gallé,Corbin et Baccarat
        sont des salles de réunion disponibles à la réservation. La salle multimédia est une salle
        dédiée aux stages de formation à inscription libre proposés par le CROSL ou pour les
        stages  organisés  par   les ligues. L'amphithéâtre  est réservable  pour  les assemblées
        générales ou pour d'autres réunions importantes. La salle de convivialité, et son office
        attenant, est également disponible à la réservation, souvent pour les repas "traiteur" qui
        suivent les réunions.  Toutes ces salles sont accessibles en soirée : un système de
        "digicode"   permet   d'entrer   dans   les   locaux   en   dehors   des   heures   d'ouverture   des
        bureaux.<br/>
        Différents services pourrontt être demandés en parallèle à la réservation : aménagement
        particulier, fourniture d’eau, de café etc. Ces services seront pour la plupart payants.
        La salle de reprographie est un espace commun aux ligues et à l'administration de la
        M2L.<br/>
        Les ligues ont  la possibilité de réserver gratuitement  l'amphithéâtre  ou la salle  de
        convivialité une fois par an et six fois les salles de réunion. Au delà, les réservations sont
        payantes.   D’autres   structures   extérieures   peuvent   réserver   l’ensemble   des   salles
        ressources moyennant finance.<br/>
        Implantation des ligues dans les bureaux<br/>
        En fonction de leur importance, les ligues et comités départementaux occupent un ou
        plusieurs bureaux. Ceux-ci peuvent communiquer entre eux quand ils sont contigus.
        Chaque bureau dispose d'un espace de rangement. Les employés et les bénévoles des
        ligues et CD disposent en bout d'étage d'une salle de réunion libre, laquelle est accessible
        seulement en journée et sans réservation
        (« premier arrivé, premier servi » ).
        La location se fait sur la base d'un forfait de charges locatives par bureau (5€ par m 2 et par mois). <br/>
