        implantation des baies de brassage<br/>
        Il y a eu un laps  de temps  important  entre  la construction  des bâtiments  A et B
        (construits au début des années 90) et C et D (tout dernièrement construits, en 2010).<br/>
        Pour le bâtiment A, on trouve une armoire de brassage secondaire pour chacun des 4
        niveaux d'implantation des bureaux de ligues. Chacune de ces armoires regroupe 22
        prises Ethernet : 20 vers les bureaux, une vers la borne Wifi et une vers la salle de
        réunion. On trouve un commutateur 26 ports dans chacune de ces armoires (24 ports
        10/100 et deux ports Giga combo RJ-45 / SFP). Ces armoires de brassage sont connectées
        à   une   armoire   centralisatrice   au   rez-de-chaussée,   dans   un   petit   local   climatisé   du
        bâtiment B.<br/>
        Pour le nouveau bâtiment C, on a positionné une seule armoire de brassage au niveau
        quasi central du second étage du bâtiment. Cette armoire réunit 128 prises, 32 par étage
        (3 par bureau, une prise pour la salle de réunion et une pour la borne wifi). On y trouve 4
        commutateurs 48 ports, dont tous les ports sont utilisables en gigabits/s. Cette armoire
        est connectée en fibre optique à l'armoire centralisatrice du bâtiment B.<br/>
        Les salles du RDC des nouveaux  bâtiments C et D (salles à réservations, bureaux
        d'administration de la M2L, la salle de reprographie) ainsi que les bornes Wifi et les 3
        écrans d'information sont tous connectés à l'armoire de brassage principale du bâtiment
        B dans laquelle on trouve 2 commutateurs à 24 ports et un routeur. C'est également le
        lieu d'implantation de l'armoire à serveurs.<br/>
