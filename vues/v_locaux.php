    <h2 class="mt-5">Informations sur les locaux de la M2L</h2>
    <p>
        Le site se compose de quatre bâtiments, dont deux dotés de quatre étages et donc de 5
        niveaux (les bâtiments A et C) et deux de plain-pied (les bâtiments B et D) dotés d’un
        seul rez-de-chaussée. Les bâtiments C et D sont neufs. Les bâtiments A et B datent d'une
        quinzaine d'années.<br/>
        Les étages des bâtiments A et C hébergent les bureaux des ligues locataires et le rez-de-
        chaussée des quatre bâtiments héberge des espaces mutualisés : un amphithéâtre de
        200 places avec une régie, 8 salles de réunion de 12 à 50 places, un hall d'accueil, une
        salle de convivialité et son office traiteur, ainsi qu'une salle de formation multimédia
        dotée de 24 postes. On y trouve également la partie «
        accueil » des neuf bureaux de
        l'administration de la M2L. L’autre partie se trouve au premier étage du bâtiment C.
        Différents locaux de service (archives, stockage local d'entretien ...) se trouvent en sous-
        sol.<br/>
        En fonction de leur taille, les 24 structures hébergées occupent un ou plusieurs bureaux.
        Au total, la M2L offre 80 bureaux. A chaque étage des bâtiments de bureaux se trouve
        une petite salle de réunion que les ligues utilisent sans réservation. L'ensemble des
        autres salles ressources sont accessibles sur réservation, en journée comme en soirée
    </p>
